    getPlayableUrl(url) {
        if (!url.includes('.m3u8')) return url;

        // Detect native HLS support (iOS / iPhone Safari)
        const isNativeHlsSupported = !Hls.isSupported() && document.createElement('video').canPlayType('application/vnd.apple.mpegurl');
        if (isNativeHlsSupported) {
            console.log("iOS/Safari Native HLS detected. Loading direct stream URL to avoid proxy issues.");
            return url;
        }

        // For browsers requiring Hls.js (PC Chrome/Firefox, Android Chrome), use the public CORS proxy (unencoded)
        console.log("PC / Android browser detected. Routing HLS stream through high-performance CORS proxy.");
        return `https://corsproxy.io/?${url}`;
    },

    initPlayer(url) {
        const video = document.getElementById('live-player');
        if (!video) return;

        if (this.hls) {
            this.hls.destroy();
        }

        const proxiedUrl = this.getPlayableUrl(url);

        if (Hls.isSupported()) {
            this.hls = new Hls({
                enableWorker: false,
                lowLatencyMode: true,
                backBufferLength: 90
            });
            this.hls.loadSource(proxiedUrl);
            this.hls.attachMedia(video);
            this.hls.on(Hls.Events.MANIFEST_PARSED, () => {
                video.play().catch(e => {
                    console.log("Autoplay blocked on Android Live TV, waiting for user tap...");
                    const playOnTap = () => {
                        video.play().catch(err => console.log(err));
                        document.removeEventListener('click', playOnTap);
                        document.removeEventListener('touchstart', playOnTap);
                    };
                    document.addEventListener('click', playOnTap);
                    document.addEventListener('touchstart', playOnTap);
                });
            });
            
            this.hls.on(Hls.Events.ERROR, (event, data) => {
                if (data.fatal) {
                    switch (data.type) {
                        case Hls.ErrorTypes.NETWORK_ERROR:
                            this.hls.startLoad();
                            break;
                        case Hls.ErrorTypes.MEDIA_ERROR:
                            this.hls.recoverMediaError();
                            break;
                        default:
                            this.hls.destroy();
                            break;
                    }
                }
            });
        } else if (video.canPlayType('application/vnd.apple.mpegurl')) {
            video.src = proxiedUrl;
            video.addEventListener('loadedmetadata', () => {
                video.play().catch(e => {
                    console.log("Native Autoplay blocked on Android Live TV, waiting for user tap...");
                    const playOnTap = () => {
                        video.play().catch(err => console.log(err));
                        document.removeEventListener('click', playOnTap);
                        document.removeEventListener('touchstart', playOnTap);
                    };
                    document.addEventListener('click', playOnTap);
                    document.addEventListener('touchstart', playOnTap);
                });
            });
        }
    }
